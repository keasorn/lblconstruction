        <h1 class="text-center" style="color:red"> java script Note</h1>
        <h3>if you want fade Carousel Use  fade: true,</h3>
        <p> For Example</p>
        <code>
            $('.slider2').slick({
            slidesToShow: 3,
            slidesToScroll: 1,
            focusOnSelect: true,
            autoplay: true,
            fade: true,
            });
        </code>

        <h3>if you Dont wnat Dots</h3>
        <p> For Example</p>
        <code>
            $('.slider2').slick({
            slidesToShow: 3,
            slidesToScroll: 1,
            focusOnSelect: true,
            autoplay: true,
            fade: true,
            dots: false,
            });
        </code>

        <h3>if you Dont wnat Arrow</h3>
        <p> For Example</p>
        <code>
            $('.slider2').slick({
            slidesToShow: 3,
            slidesToScroll: 1,
            focusOnSelect: true,
            autoplay: true,
            fade: true,
            dots: false,
            arrows: false,
            });
        </code>

        <h3>if you want center div active</h3>
        <p> For Example</p>
        <code>
            $('.slider2').slick({
            slidesToShow: 3,
            slidesToScroll: 1,
            focusOnSelect: true,
            autoplay: true,
            centerMode: true,
            fade: true,
            dots: false,
